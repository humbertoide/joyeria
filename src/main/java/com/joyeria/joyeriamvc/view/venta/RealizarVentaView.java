    private void fillTablaCliente() {
        DefaultTableModel tableModel = (DefaultTableModel) datosCliente.getModel();
        tableModel.setRowCount(0);
        ClienteVO cliente = (ClienteVO) comboBoxCliente.getSelectedItem();
        if (cliente == null) {
            return;
        }
        tableModel.addRow(new Object[]{
            cliente.getId(),
            cliente.getName()
        });
        tableModel.fireTableDataChanged();
    }

    class customRenderer extends DefaultListCellRenderer {

        @Override
        public Component getListCellRendererComponent(JList<?> list, Object value, int index, boolean isSelected, boolean cellHasFocus) {
            super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus);

            if (value instanceof JoyaVO) {
                JoyaVO joya = (JoyaVO) value;
                setText(joya.getName());
            }
            if (value instanceof ClienteVO) {
                ClienteVO cliente = (ClienteVO) value;
                setText(cliente.getName());
            }

            return this;
        }
    }

