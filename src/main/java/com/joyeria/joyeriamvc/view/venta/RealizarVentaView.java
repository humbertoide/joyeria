    public void fillComboBoxCliente(List<ClienteVO> clientes) {
        comboBoxCliente.removeAllItems();
        for (ClienteVO cliente : clientes) {
            comboBoxCliente.addItem(cliente);
        }
        comboBoxCliente.setSelectedIndex(-1);
    }

    public void fillComboBoxJoya(List<JoyaVO> joyas) {
        comboBoxJoya.removeAllItems();
        for (JoyaVO joya : joyas) {
            comboBoxJoya.addItem(joya);
        }
        comboBoxJoya.setSelectedIndex(-1);
    }

    private void fillTablaJoya() {
        DefaultTableModel tableModel = (DefaultTableModel) datosJoya.getModel();
        tableModel.setRowCount(0);
        JoyaVO joya = (JoyaVO) comboBoxJoya.getSelectedItem();
        if (joya == null) {
            return;
        }
        tableModel.addRow(new Object[]{
            joya.getId(),
            joya.getName(),
            joya.getMaterial(),
            joya.getWeight_gr(),
            joya.getPrice(),
            joya.getStock()
        });
        tableModel.fireTableDataChanged();
    }

