    public int getSelectedJoyaId() {
        int rowIndex = tablaInventarioJoyas.getSelectedRow();
        if (rowIndex == -1) {
            return -1;
        }
        return (int) tablaInventarioJoyas.getValueAt(rowIndex, 0);

    }

